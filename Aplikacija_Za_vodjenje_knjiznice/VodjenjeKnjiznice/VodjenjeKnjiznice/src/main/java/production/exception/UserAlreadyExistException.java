package production.exception;

import java.io.Serial;
import java.io.Serializable;

public class UserAlreadyExistException extends Exception implements Serializable {
    @Serial
    private static final long serialVersionUID = -6924573110819982210L;

    public UserAlreadyExistException() {
    }

    public UserAlreadyExistException(String message) {
        super(message);
    }

    public UserAlreadyExistException(String message, Throwable cause) {
        super(message, cause);
    }

    public UserAlreadyExistException(Throwable cause) {
        super(cause);
    }

    public UserAlreadyExistException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
